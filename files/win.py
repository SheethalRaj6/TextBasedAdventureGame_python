import turtle   #depending on the character selected each function has a different end  
def commanderwin():
    screen=turtle.Screen()
    screen.tracer(0)
    screen.colormode(255)
    turtle.bgpic("commanderwin.gif")
    screen.title("The Commander has won the game!")
    screen.setup(width=1000, height=800)
    
